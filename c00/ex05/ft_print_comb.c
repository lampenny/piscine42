#include <unistd.h>

void	ft_print_comb(void)
{
	int	u1;
	int	u2;
	int	u3;

	u1 = '0';
	while (u1 <= '7')
	{
		u2 = u1 + 1;
		while (u2 <= '8')
		{
			u3 = u2 + 1;
			while (u3 <= '9')
			{
				write(1, &u1, 1);
				write(1, &u2, 1);
				write(1, &u3, 1);
				if (!(u1 == '7' && u2 == '8' && u3 == '9'))
					write(1, ", ", 2);
				u3++;
			}
			u2++;
		}
		u1++;
	}
}
